# Declare a variable with value 2
my_number = 2

# Check if the variable is equal to 2
if my_number == 2:
    print("Number is Two")
else:
    print("Number is not Two")

# Change the value of the variable
my_number = 3

# Check again and print the appropriate message
if my_number == 2:
    print("Number is Two")
else:
    print("Number is not Two")
