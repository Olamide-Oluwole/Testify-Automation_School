# Declare two numbers with any values
number1 = 10
number2 = 5

# Add the two numbers
sum_number = number1 + number2
print(f"Addition Result: {sum_number}")

# Subtract the second number from the first
subtract_number = number1 - number2
print(f"Subtraction Result: {subtract_number}")

# Multiply the two numbers
multiply_result = number1 * number2
print(f"Multiplication Result: {multiply_result}")
